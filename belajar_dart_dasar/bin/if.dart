void main() {
  var nilai1 = 90;
// if statement
  if(nilai1 == 90) {
    print("anda lulus");
  }

  var nilai2 = 70;

// if else statement
  if(nilai2 >= 70) {
    print("anda lulus");
  } else {
    print("anda tidak lulus");
  }

  var nilai3 = 50;
  // else if statement
  if(nilai3 >= 80) {
    print("anda lulus dengan nilai baik");
  } else if(nilai3 == 60) {
    print("anda lulus");
  } else {
    print("anda tidak lulus");
  }
}