void main() {
  // data type int can't be change to double
  int number1 = 10;
  // and data type double can't be change to int
  double number2 = 10.5;

  print(number1);
  print(number2);

  // but num can be change to int or double
  num number3 = 14;
  print(number3);

  // number3 has changed from int to double
  number3 = 14.5;
  print(number3);
}
